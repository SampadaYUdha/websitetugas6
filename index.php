            <h2 class="h2 section-title">Mengenal <br/> Subak Jatiluwih</h2>

            <p class="section-text">
             Jatiluwih adalah sebuah desa yang mempunyai daerah hamparan persawahan luas dengan panorama sawah bertingkat yang indah yang terletak di wilayah Kecamatan Penebel, Kabupaten Tabanan Bali. Desa Jatiluwih terkenal sebagai tempat wisata dengan keindahan sawah terasering yang masih menggunakan sistem pengairan sawah tradisional Bali, lokasinya terletak dekat dengan pegunungan Batukaru dengan kondisi udara yang lumayan sejuk.
           </p>

           <p class="section-text">
            Subak Jatiluwih juga telah ditetapkan sebagai salah satu warisan dunia oleh UNESCO sejak 6 Juli 2012. Bentangan terasering lahan padi di Subak Jatiluwih dari area perbukitan hingga lembah terkenal sebagai destinasi wisata global yang menyuguhkan keindahan serta ketenangan alam Bali sebagai representative sistem pertanian Indonesia. Subak Jatiluwih berhasil menjalankan 5 fungsi dari subak itu sendiri yaitu kegiatan upacaara keagamaan,pembagian dan pendistribusian air irigasi, operasional dan pemeliharaan jaringan irigasi, mobilisasi sumberdaya, penanganan konflik.
